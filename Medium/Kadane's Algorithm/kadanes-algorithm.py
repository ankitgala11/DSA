class Solution:
    ##Complete this function
    #Function to find the sum of contiguous subarray with maximum sum.
    def maxSubArraySum(self,arr,N):
        ##Your code here
        
        currsum=arr[0]
        ans=currsum
        
        for i in range(1, N):
            currsum+=arr[i]
            
            currsum=max(currsum, arr[i])
            ans=max(ans, currsum)
            
            
        return ans


